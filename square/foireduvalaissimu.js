function addScoreTableau() {
	
  var table = document.getElementById("tableauScore");  
  var matrix=new Array();
	//Récupère toutes les champs de localstorage
  for (var i = 0; i < localStorage.length; i++) {

	  
	  var key = localStorage.key(i);
	  var value = localStorage.getItem(key);
	  
	  //Sélectionne les lignes et colonnes d'ou on va ajouter le score
	  
	  
	  //console.log('Utilisateur: ' + key + ', Score: ' + value);  
	  matrix[i]=new Array(2);
	  matrix[i][0] = key;
	  matrix[i][1] = value;

	}
	matrix.sort(compareSecondColumn);
	for(var i= 0; i<matrix.length; i++){
		var row = table.insertRow(2);
		var cell1 = row.insertCell(0);
		var cell2 = row.insertCell(1);
	  
	  
	  cell1.innerHTML = matrix[i][0];
	  cell2.innerHTML = matrix[i][1];
	}
}

function compareSecondColumn(a, b) {
    if (a[1] === b[1]) {
        return 0;
    }
    else {
        return (a[1] < b[1]) ? -1 : 1;
    }
}


